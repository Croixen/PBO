    private void btnConfirmEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmEditActionPerformed
        // TODO add your handling code here:
        clsService Edit = new clsService();
       if(CboStatus.getSelectedItem().toString().equals("Produk sudah selesai diperbaiki") && cboProgress.getSelectedItem().toString().equals("Selesai")){
           try{
            Edit.Selesai(ServiceTable.getValueAt(ServiceTable.getSelectedRow(), 0).toString(), CboStatus.getSelectedItem().toString(), cboProgress.getSelectedItem().toString());
            Edit.stat.executeUpdate(Edit.sql);
            toogle = false;
            clear();
            btnCancel.setVisible(false);
            btnEdit.setVisible(true);
            btnConfirmEdit.setVisible(false);
            IsiTabel();
        }catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Gagal melakukan update terhadap database");
        }
       }else if(CboStatus.getSelectedItem().toString().equals("Produk batal untuk dilakukan perbaikkan") && cboProgress.getSelectedItem().toString().equals("Batal")){
            try{
            Edit.Selesai(ServiceTable.getValueAt(ServiceTable.getSelectedRow(), 0).toString(), CboStatus.getSelectedItem().toString(), cboProgress.getSelectedItem().toString());
            Edit.stat.executeUpdate(Edit.sql);
            toogle = false;
            
            clear();
            btnCancel.setVisible(false);
            btnEdit.setVisible(true);
            IsiTabel();
            }catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Gagal melakukan update terhadap database");
        }
       }else{
           try{
            Edit.edit(ServiceTable.getValueAt(ServiceTable.getSelectedRow(), 0).toString(), CboStatus.getSelectedItem().toString(), cboProgress.getSelectedItem().toString());
            Edit.stat.executeUpdate(Edit.sql);
            toogle = false;
            clear();
            btnCancel.setVisible(false);
            btnEdit.setVisible(true);
            IsiTabel();
            }catch(Exception e){
            System.out.println(e.getMessage());
            JOptionPane.showMessageDialog(rootPane, "Gagal melakukan update terhadap database");
        }
       }
    }//GEN-LAST:event_btnConfirmEditActionPerformed

