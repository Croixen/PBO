    private void IsiTabel(){
        clsService tablefiller = new clsService();
        DefaultTableModel filler = new DefaultTableModel();
        filler.addColumn("SID");
        filler.addColumn("SKU");
        filler.addColumn("Brand");
        filler.addColumn("Nama Pelanggan");
        filler.addColumn("Tanggal Masuk");
        filler.addColumn("Status");
        filler.addColumn("Nama Petugas");
        filler.addColumn("Progress");
        filler.addColumn("Tanggal Selesai");
        try{
            tablefiller.show(ID);
            ResultSet rs = tablefiller.stat.executeQuery(tablefiller.sql);
            while(rs.next()){
                if(rs.getString(9) == null||rs.getString(9).equals("0000-00-00")||rs.getString(9).equals("")){
                    filler.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getDate(5), rs.getString(6), rs.getString(7), rs.getString(8), ""}); 
                }else{
                    filler.addRow(new Object[]{rs.getInt(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getDate(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9)}); 
                }
                    
            }
            ServiceTable.setModel(filler);
            ServiceTable.getTableHeader().setResizingAllowed(false);
            ServiceTable.setDefaultEditor(Object.class, null);
            setTableHeader();
        }catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Error Fetching");
            System.out.println(e.getMessage());
        }
    }
    
    
